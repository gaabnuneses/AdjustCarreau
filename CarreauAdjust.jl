## Exclui dados fora dos limites em x
function Limite(x,y,xmin,xmax)
    X = []
    Y = []
    for i in 1:length(x)
        if x[i]>xmin && x[i]< xmax
            X=[X;x[i]]
            Y=[Y;y[i]]
        end
    end
    return X,Y #
end

## Calcula os erros parciais
function erro(x,y,n,λ,μ₀,μ∞)
    N = length(x)
    ϵn = 0
    ϵλ = 0
    ϵμ₀ = 0
    ϵμ∞ = 0
    for i in 1:N
        ϵn += ((μ∞-y[i])*(λ^2*x[i]^2+1)^((1/2)*n-1/2)+(μ₀-μ∞)*(λ^2*x[i]^2+1)^(n-1))*(μ₀-μ∞)*log(λ^2*x[i]^2+1)
        ϵλ += x[i]^2*(μ₀-μ∞)*((μ∞-y[i])*(λ^2*x[i]^2+1)^((1/2)*n-3/2)+(λ^2*x[i]^2+1)^(n-2)*(μ₀-μ∞))
        ϵμ₀ += (μ∞-y[i])*(λ^2*x[i]^2+1)^((1/2)*n-1/2)+(μ₀-μ∞)*(λ^2*x[i]^2+1)^(n-1)
        ϵμ∞ +=  (μ∞-y[i])*(λ^2*x[i]^2+1)^((1/2)*n-1/2)+(μ₀-μ∞)*(λ^2*x[i]^2+1)^(n-1)
    end
    # ϵn = ϵn
    ϵλ = ϵλ*(2*(n-1))*λ
    # println(ϵλ)
    ϵμ₀ = ϵμ₀*2
    ϵμ∞ = ϵμ∞*2 + (2*N+2)*μ∞

    return ϵn,ϵλ,ϵμ₀,ϵμ∞
end

## Atualiza coeficientes
function calCoef(x,y,n,λ,μ₀,μ∞,nit,h=.01)
    for i in 1:nit
        ϵn,ϵλ,ϵμ₀,ϵμ∞ = erro(x,y,n,λ,μ₀,μ∞)
        n = n - h*ϵn
        # println(n)
        λ = λ - h*ϵλ
        # println(λ)
        μ₀ = μ₀ - h*ϵμ₀
        μ∞ = μ∞ - h*ϵμ∞
    end
    return n,λ,μ₀,μ∞
end

